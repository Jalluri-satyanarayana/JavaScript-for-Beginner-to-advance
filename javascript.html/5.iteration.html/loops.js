//  for loop 
for(let i=0;i<5;i++){
    console.log('satya',i);
}
// output :
// satya 0
// satya 1
// satya 2
// satya 3
// satya 4

// While loop
let x = 0;
 while (x<5){
    console.log("satya",x);
    x++;
 }
// output :
// satya 0
// satya 1
// satya 2
// satya 3
// satya 4

// do - while loop
let i = 0;
do{
    i++;
    console.log(i);
}
while(i<5);