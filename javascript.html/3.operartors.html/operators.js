//Comparison operators :  >, <, >=, <=, !=, ==, ===  follow these same as below operators are '>' , '==', '==='
var num1 = 10;
var num2 = 20;
result = num1 > num2  // 10 > 20
console.log(result);  // false

//using '==' operator
var num1 = '20';
var num2 = 20;
result = num1 == num2  // '20' == 20
console.log(result); // true

//using '===' operator
var num1 = '20';
var num2 = 20;
result = num1 === num2  // '20' == 20
console.log(result); // false

// Logical operators && , || , ! 

